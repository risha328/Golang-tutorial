package main

import "fmt"

func main() {
	fmt.Println(1)
	//int
	fmt.Println(1 + 1)
	//float
	fmt.Println(1.5 + 1.5)
	fmt.Println(1.5 * 2)
	fmt.Println(1.5 / 2)
	//string
	fmt.Println("Hello" + " " + "World")
	//bool
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	//variable
	var x int = 10
	fmt.Println(x)

}
